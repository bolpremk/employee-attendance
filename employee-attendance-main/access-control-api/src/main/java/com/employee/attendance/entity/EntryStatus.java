package com.employee.attendance.entity;

/**
 * Enum representing the entry status of an employee event.
 * Possible values are SWIPE_IN and SWIPE_OUT.
 */
public enum EntryStatus {
    /**
     * Represents the entry status when an employee swipes in.
     */
    SWIPE_IN,

    /**
     * Represents the entry status when an employee swipes out.
     */
    SWIPE_OUT
}
